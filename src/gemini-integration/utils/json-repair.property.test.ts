/**
 * Property-based tests for JSON Repair — Property 8: JSON Repair Idempotence
 *
 * For any valid JSON object, applying the JSON repair function SHALL return
 * the same object unchanged (repair is idempotent on valid input).
 *
 * **Validates: Requirements 5.4**
 */
import { describe, it, expect } from 'vitest';
import fc from 'fast-check';

import { repairJson } from './json-repair.js';

describe('JSON Repair Idempotence (Property 8)', () => {
  /**
   * **Validates: Requirements 5.4**
   * For any valid JSON object, repairJson SHALL return success: true,
   * the same object as repairedJson, and an empty repairs array.
   */
  it('returns valid JSON objects unchanged with no repairs', () => {
    fc.assert(
      fc.property(
        fc.dictionary(fc.string({ minLength: 1, maxLength: 20 }), fc.jsonValue()),
        (obj) => {
          const raw = JSON.stringify(obj);
          const result = repairJson(raw);

          expect(result.success).toBe(true);
          expect(result.repairedJson).toEqual(obj);
          expect(result.repairs).toEqual([]);
        },
      ),
      { numRuns: 100 },
    );
  });

  /**
   * **Validates: Requirements 5.4**
   * For any valid JSON array, repairJson SHALL return success: true,
   * the same array as repairedJson, and an empty repairs array.
   */
  it('returns valid JSON arrays unchanged with no repairs', () => {
    fc.assert(
      fc.property(fc.array(fc.jsonValue()), (arr) => {
        const raw = JSON.stringify(arr);
        const result = repairJson(raw);

        expect(result.success).toBe(true);
        expect(result.repairedJson).toEqual(arr);
        expect(result.repairs).toEqual([]);
      }),
      { numRuns: 100 },
    );
  });

  /**
   * **Validates: Requirements 5.4**
   * For any valid JSON object or array generated by fc.jsonValue(), repairJson SHALL
   * parse it successfully and return the equivalent value with no repairs.
   * Note: repairJson only handles objects/arrays (not primitives), matching its
   * return type of `object | null`.
   */
  it('returns arbitrary valid JSON objects/arrays unchanged with no repairs', () => {
    const jsonObjectOrArrayArb = fc.jsonValue().filter((v) => v !== null && typeof v === 'object');

    fc.assert(
      fc.property(jsonObjectOrArrayArb, (value) => {
        const raw = JSON.stringify(value);
        const result = repairJson(raw);

        expect(result.success).toBe(true);
        expect(result.repairedJson).toEqual(value);
        expect(result.repairs).toEqual([]);
      }),
      { numRuns: 100 },
    );
  });

  /**
   * **Validates: Requirements 5.4**
   * For any valid JSON object with nested structures, repairJson SHALL
   * preserve the full structure with no repairs needed.
   */
  it('preserves nested JSON objects with no repairs', () => {
    const nestedObjectArb = fc.record({
      id: fc.integer(),
      name: fc.string({ minLength: 1, maxLength: 50 }),
      active: fc.boolean(),
      tags: fc.array(fc.string({ minLength: 1, maxLength: 10 }), { maxLength: 5 }),
      metadata: fc.dictionary(
        fc.string({ minLength: 1, maxLength: 10 }),
        fc.oneof(fc.string(), fc.integer(), fc.boolean(), fc.constant(null)),
      ),
    });

    fc.assert(
      fc.property(nestedObjectArb, (obj) => {
        const raw = JSON.stringify(obj);
        const result = repairJson(raw);

        expect(result.success).toBe(true);
        expect(result.repairedJson).toEqual(obj);
        expect(result.repairs).toEqual([]);
      }),
      { numRuns: 100 },
    );
  });
});
